package model;

class FastFood {
    private String foodStyle;
    private String knownFood;

    public String getFoodStyle() {
        return this.foodStyle;
    }

    public void setFoodStyle(String foodStyle) {
        this.foodStyle = foodStyle;
    }

    public String getKnownFood() {
        return this.knownFood;
    }

    public void setKnownFood(String knownFood) {
        this.knownFood = knownFood;
    }
}

